package com.example.shoppingapp.CategoriesProductDetails;

public class LeftCategoryModel {

    private int image;
    private String name;

    public LeftCategoryModel(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
